package corejava;

public class Literals {

    public static void main(String[] args) {
        int decimal = 100;
        int binary = 0b1101;
        int hex = 0x1A;
        char ch = 'A';
        String str = "Hello";
        System.out.println(decimal + ", " + binary + ", " + hex + ", " + ch + ", " + str);
    }
}
